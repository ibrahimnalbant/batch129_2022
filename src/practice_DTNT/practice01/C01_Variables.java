package practice_DTNT.practice01;

public class C01_Variables {

    public static void main(String[] args) {

        // TECHPRO EDUCATION ==> her bir harf alt alta gelecek sekilde yazdiran kodu olusturunuz.

        // noktalı virgülden sonra imleç oradaysa, ctrl + D  ile satır aşağıya doğru çoğaltılabilir.

        System.out.println("**********1. YOL*********");
        System.out.println("I");
        System.out.println("B");
        System.out.println("R");
        System.out.println("A");
        System.out.println("H");
        System.out.println("I");
        System.out.println("M");
        System.out.println(" ");
        System.out.println("N");
        System.out.println("A");
        System.out.println("L");
        System.out.println("B");
        System.out.println("A");
        System.out.println("N");
        System.out.println("T");

        /*

        \n  ==>  alt satıra geçirir
        \t ==>  bir tab boşluk bırakır
        \b  ==>  gerisindeki ilk harfi siler
        ctrl + alt + l  ==> sayfayı düzenler

        */

        // yukarıdaki örneği tek sout ile her harf alt alta gelecek sekilde yazdiriniz.

        System.out.println("********2. YOL********");

        System.out.println("\nI\nB\nR\nA\nH\nI\nM\n\nN\nA\nL\nB\nA\nN\nT");

        //3) JAVA ILE GUZEL DUNYA her bir kelimeyi alt satira yazdiracak sekilde kodu create ediniz

        System.out.println("\nJAVA\nILE\nGUZEL\nDUNYA");
        System.out.println("JAVA");
        System.out.println("ILE");
        System.out.println("GUZEL");
        System.out.println("DUNYA");

        //4) JAVA ILE GUZEL DUNYA seklinde kelimeler arasinda 1 tab bosluk birakan kodu yaziniz
        System.out.println("JAVA\tILE\tGUZEL\tDUNYA");

        //5) pazartesi kelimesinin her bir harfini alt satirda yazdiracak sekilde tek sout ile kodu create ediniz
        System.out.println("\np\na\nz\na\nr\nt\ne\ns\ni");

        //Soru-6 : "Techpro" ile java cok 'kolay'   yazdiran bir kod yaziniz
            // sout çıktısında çift tırnağın gözükmesini istiyorsan \" .... \" yazmalısın
            // sout çıktısında tek tırnağın gözükmesini istiyorsan \' .... \' yazmalısın.yazmasanda çalışıyor.
        System.out.println("\"Techpro\"\tile\tjava\tcok\t'kolay'");

        //Soru-7 : "MAHARET" hic 'DUSMEMEK'  degil; "Her dustugunde kalkabilmektir" ==>ifadesini

        //   seklinde noktali virgulden sonra 3 satir asagidan ve paragraf basi yaparak yazdiran bir kod yaziniz.

        System.out.println("\"MAHARET\" hic\t'DUSMEMEK'\tdegil;\n\n\n\t\"Her dustugunde kalkabilmektir\"");

    }
}
